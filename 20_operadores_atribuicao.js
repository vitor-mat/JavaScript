//A atribuicao pode ser feita pelo simbolo de "=" na programacao. Indica que um respectivo valor pertence-a.
const a = 7
let b = 3

//E ainda possivel fazer operacoes a partir do simbolo de atribuicao

b += a // b = b + a
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /=3 // b = b / 3
console.log(b)

b %=2 // b = b % 2 (resto da divisao)
console.log(b)

//Voce primeiro insere o simbolo de operacao depois o de atribuicao (ex: +=)